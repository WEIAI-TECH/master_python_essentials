# one variable
name = 'Sue'
print('Hello, {}!'.format(name))

# more than one variable
name = 'Sue'
age = 20
print('{} is {} year old.'.format(name,age))

# for a list
aList = [1,2,3]
print('This is a list: {}.'.format(aList))

# for float
x = 1
y = 3
z = x/y

print('The result of {} divided by {} is {}.'.format(x,y,z))
print('The result of {} divided by {} with one decimal place is {:.1f}.'.format(x,y,z))
print('The result of {} divided by {} with three decimal places is {:.3f}.'.format(x,y,z))


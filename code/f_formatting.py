# one variable
name = 'Sue'
print(f'Hello, {name}!')

# more than one variable
name = 'Sue'
age = 20
print(f'{name} is {age} year old.')

# for a list
aList = [1,2,3]
print(f'This is a list: {aList}.')

# for float
x = 1
y = 3
z = x/y

print(f'The result of {x} divided by {y} is {z}.')
print(f'The result of {x} divided by {y} with one decimal place is {z:.1f}.')
print(f'The result of {x} divided by {y} with three decimal places is {z:.3f}.')


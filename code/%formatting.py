# one varaible
name = 'Sue'
print('Hello, %s!'%name)

# more than one variable
name = 'Sue'
age = 20
print('%s is %d year old.'%(name,age))

# for a list
aList = [1,2,3]
print('This is a list: %s.'%aList)

# for float
x = 1
y = 3
z = x/y

print('The result of %d divided by %d is %f.'%(x,y,z))
print('The result of %d divided by %d with one decimal place is %.1f.'%(x,y,z))
print('The result of %d divided by %d with three decimal places is %.3f.'%(x,y,z))

